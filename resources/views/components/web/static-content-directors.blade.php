            <h3 class="text-center font-extrabold text-4xl text-gray-700 my-6">Padre Daniel Saldarriaga Molina</h3>
            <p class="text-base text-gray-800 mb-4">
                Padre Daniel Saldarriaga Molina, fundador del Banco de Alimentos de Bogotá. Nació en Medellín un 
                sábado santo, hijo de Pedro Alberto Saldarriaga y Anita Molina, el mayor de cuatro hermanos.
            </p>
            <p class="text-base text-gray-800 mb-4">
                Terminó sus estudios básicos de primaria y secundaria a temprana edad, para luego entrar al 
                Seminario Conciliar de Bogotá, donde realizó tres años de filosofía y cuatro de teología. Se 
                ordenó como sacerdote el 25 de noviembre de 1989.
            </p>
            <p class="text-base text-gray-800 mb-4">
                El Padre Daniel Saldarriaga Molina se define como un sacerdote para la gloria de Dios.
                Aquí resumimos algunos de sus trabajos ministeriales: En 1990 y hasta finales de 1991 
                fue vicario parroquial de San Luis Beltrán, en el Barrio Polo Club. Luego, entre 1992 
                y 2002 siguió como párroco Madre de la Divina Gracia en Suba Tibabuyes. En el 2001 bajo 
                el liderazgo de Monseñor Pedro Rubiano, fundo el Banco Arquidiocesano de Alimentos de 
                Bogotá, y desde entonces asumió como Director Ejecutivo. De igual manera, del 2002 hasta 
                el 2006 continuó con su trabajó como párroco en Nuestra Señora de la Peña, en el barrio 
                Los Laches y el Santuario de Guadalupe. Entre el 2006 y 2009, fue párroco de Santa María 
                Goretti y entre el 2009 y 2012 fue rector del Santuario de Monserrate. Desde entonces y 
                hasta ahora es párroco en la parroquia de San Maximiliano Kolbe y director ejecutivo del 
                Banco de Alimentos de Bogotá.
            </p>
            <p class="text-base text-gray-800 mb-4">
                Él se caracteriza por ser un hombre bondadoso, visionario y persistente. Con frecuencia 
                cita frases y cuentos que marcan su vida recordando que Jesús enseñó a sus discípulos y 
                a las multitudes las más grandes lecciones de esta forma. Una de las frases que lo identifica 
                y repite es de San Pablo, que dice: “Es más feliz el que da que el que recibe” y su vida 
                cotidiana es un esfuerzo por hacer realidad este lema, porque con agradecimiento y solidaridad 
                acoge en su corazón a todas las personas que están dispuestas a ayudar y que necesitan ayuda.
            </p>
